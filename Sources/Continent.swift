/// A continent that is any of several large geographical regions.
///
/// The list of continents: `Asia`, `Africa`, `Europe`, `NorthAmerica`, `SouthAmerica`, `Australia` and `Antartica`.
public enum WPContinent: CaseIterable {
    case Asia
    case Africa
    case Europe
    case NorthAmerica
    case SouthAmerica
    case Australia
    case Antartica
}
